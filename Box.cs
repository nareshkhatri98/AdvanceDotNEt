using System;


namespace AdvanceDotnet
{
     class Box
    {
        private double length;
        private double breadth;
        private double height;

        public double Length
        {
            get { return length; }
            set { length = value; }
        }
        
        public double Breadth
        {
            get { return breadth;}
            set { breadth = value; }
        }
        public double Height
        {
            get { return height; }
            set { height = value; }
        }
    }
}
